//boton para guardar datos
    public void GuardarDatos() {
        //atributos
        ExperienciaLaboral experienciaLaboral = new ExperienciaLaboral();
        ExperienciaLaboralTXT experienciaLaboralTXT = new ExperienciaLaboralTXT(experienciaLaboral);

//asignar los datos recolectados por los textField a los atritutos de la clase ExperienciaLaboral
        experienciaLaboral.setId(textoID.getText());
        experienciaLaboral.setNombreEmpresa(textoNombreEmpresa.getText());
        experienciaLaboral.setPuesto(textoPuesto.getText());
        experienciaLaboral.setFechaInicio(textoFechaInicio.getDate());
        experienciaLaboral.setFechaFin(textoFechaFin.getDate());
        experienciaLaboral.setDescripcion(textoDescripcion.getText());

        experienciaLaboralTXT.crearArchivo();//llamado de funcion para crear archivo
        experienciaLaboralTXT.AgregarDatos();//llamado de funcion para agregar los datos en el archivo txt
    }

