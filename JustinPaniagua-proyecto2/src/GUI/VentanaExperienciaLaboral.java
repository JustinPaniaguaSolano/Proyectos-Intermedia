    private void botonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonGuardarActionPerformed
        //validar si un campo esta vacio sacado de:
        //https://www.youtube.com/watch?v=jDk3dN3ysp4&ab_channel=C%C3%B3digoXtremo
        //if else para validar que el usuario ingrese los datos necesarios
        if (textoID.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor ingresar ID", "Error", JOptionPane.WARNING_MESSAGE);
        } else if (textoNombreEmpresa.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor ingresar Nombre de la empresa", "Error", JOptionPane.WARNING_MESSAGE);
        } else if (textoPuesto.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor ingresar Puesto", "Error", JOptionPane.WARNING_MESSAGE);
        } else if (textoFechaInicio.getDate() == null) {
            JOptionPane.showMessageDialog(null, "Por favor ingresar Fecha inicio", "Error", JOptionPane.WARNING_MESSAGE);
        } else if (textoFechaFin.getDate() == null) {
            JOptionPane.showMessageDialog(null, "Por favor ingresar Fecha Fin", "Error", JOptionPane.WARNING_MESSAGE);
        } else if (textoDescripcion.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor ingresar Descripcion", "Error", JOptionPane.WARNING_MESSAGE);

        } else {
            GuardarDatos();
            JOptionPane.showMessageDialog(null, "Guardado con exito");
        }
    }//GEN-LAST:event_botonGuardarActionPerformed
