    private void cargarDatosCombox1() {
        //atributos
        DatosPersonales datosPersonales = new DatosPersonales();
        DatosPersonalesTXT datostxt = new DatosPersonalesTXT(datosPersonales);
        datostxt.leerDatos();//llamado de la funcion
        //recorrer un list sacado de:
        //https://www.arquitecturajava.com/java-foreach-y-sus-opciones/
        List<String> listaDatos = datostxt.leerDatos();//crear un lista y asignarle lo de la clase datostxt.leerDatos
        
        for (String datos : listaDatos) {//for each para recorrer la lista
            jComboBox1.addItem(datos);//agregar datos en el combobox
        }
    }

